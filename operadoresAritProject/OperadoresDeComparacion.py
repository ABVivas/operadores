#ejemplo operadores de comparacion
m = int(input("ingrese el valor de m "))
n = int(input("ingrese el valor de n "))

print(f"la igualdad es: {(m == n)}") #igualdad
print(m > n) #mayor que
print(m < n) #menor que
print(m >= n) #mayor o igual que
print(m <= n) #menos o igual que
print(m != n) #no igual

